def cifradoCesar(clave, textoParaCifrar) -> str:
    abecedarioCifrado = ""
    textoCifrado = ""
    for letras in abecedario:
        try:
            indiceLetra = abecedario.index(letras)
            # el indice va recorriendo las letras del abecedario
            # se le suma al abcedario cifrado la letra que esta en el indice menos el tamaño de la clave
            abecedarioCifrado += abecedario[indiceLetra - clave]
        except:
            pass

    for letra in textoParaCifrar:
        try:
            if (letra != " "):
                indiceLetra = abecedario.index(letra)
                letraCifrada = abecedarioCifrado[indiceLetra]
                textoCifrado += letraCifrada
            else:
                textoCifrado += " "
        except:
            textoCifrado += letra

    return textoCifrado


